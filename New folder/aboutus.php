           <font size=6 color="#9966CC"><u>ABOUT US<br/></u></font>
WEBTECH PVT LTD is a dynamic endeavour of The WEBTCH Group in the field of technological education. Having started its journey in 2002, the Institute has seen significant positive transformation after being taken over by the WEBTECH PVT LTD Foundation in 2008.<br/><br/>

Sarunay Patel, the Chairman, had long nurtured a dream of leveraging the Group’s potential in the advancement of education. Thus, as a representative of WEBTECH PVT LTD initiative, WEBTECH PVT LTD seeks to epitomize distinction and integrity in the field of academics.<br/><br/>

WEBTECH PVT LTD is committed to the pursuit of excellence and aims to attain holistic development for all students. By providing a truly inspirational academic environment, the Institute ensures that its students emerge as responsible citizens with a strong allegiance to collective advancement.<br/><br/>

<font size=6 color="#9966CC"><u>Programmes and Infrastructure an outline<br/><br/></u></font>

WEBTECH PVT PVT offers facilities and programmes for budding next-generation technocrats and has an integrated, expansive campus. The WEBTECH PVT LTD Foundation’s initial focus has been on facilities and infrastructure enhancement to suit the pace of growing technical needs.<br/><br/>

At present, the Institute offers Degree Programmes in Php, Core Java,Advance Java,Python,DotNet(.net),MySql,Database,Ruby . The renowned academicians and professionals at WEBTECH PVT LTD employ scientific teaching and assessment methods, which are further supplemented by manifold co-curricular events and other enriching activities.<br/><br/>

